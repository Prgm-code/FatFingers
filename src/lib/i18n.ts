export type TranslationKey = keyof typeof translations.en;

const actionKeys: Record<WritingAction, TranslationKey> = {
  correct: "actionCorrect",
  professional: "actionProfessional",
  shorten: "actionShorten",
  friendly: "actionFriendly",
  translate_english: "actionTranslateEnglish",
  translate_spanish: "actionTranslateSpanish",
  quick_reply: "actionQuickReply",
  custom: "actionCustom",
};

const modeKeys: Record<WritingMode, TranslationKey> = {
  plain_text: "modePlainText",
  balanced: "modeBalanced",
  formal: "modeFormal",
  creative: "modeCreative",
};

export function t(language: AppLanguage, key: TranslationKey): string {
  return translations[language][key];
}

export function writingActionLabel(language: AppLanguage, action: WritingAction): string {
  return t(language, actionKeys[action]);
}

export function writingModeLabel(language: AppLanguage, mode: WritingMode): string {
  return t(language, modeKeys[mode]);
}

export function formatMessage(
  language: AppLanguage,
  key: TranslationKey,
  values: Record<string, string | number>,
): string {
  return Object.entries(values).reduce(
    (message, [name, value]) => message.split(`{${name}}`).join(String(value)),
    t(language, key),
  );
}
